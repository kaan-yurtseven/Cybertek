package saturdayClase;

public class CountingDown {

	public static void main(String[] args) {
		//count from 10 to 0 using 3 type of loops
		//
//		while loop
		int x=10;
		while(x>=0) {
			System.out.print(x+ " ");
			x--;
		}System.out.println();
	
//	    for loop
	
		for(int y=10;y>=0;y--) {
			System.out.print(y+" ");
		}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}
	
	

}
