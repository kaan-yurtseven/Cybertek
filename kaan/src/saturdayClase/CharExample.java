package saturdayClase;

public class CharExample {

	public static void main(String[] args) {
	    
	    for(char endingChar = 'A' ; endingChar<= 'Z' ; endingChar++) {
	      
	       for( char letter = 'A' ; letter <= endingChar ; letter++ ) {
	         System.out.print( letter + " ");
	       }
	       System.out.println();
	    
	    }
	}

}
